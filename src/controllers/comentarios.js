'use strict';

const Comentarios 	= require('../collections/comentarios');
const Comentario  	= require('../models/comentario');

function getComentarios(req, res, next) {
	Comentarios.query(function (qb) {
   		qb.where('comentario.estatus', '=', 1);
	})
	.fetch({ withRelated: [
		'cliente',
		'cliente.estado',
		'cliente.estado_civil',
		'cliente.genero',
		'cliente.rango_edad',
		'respuesta',
		'respuesta.tipo_respuesta',
		'tipo_comentario'
		]})
	.then(function(data) {
		if (!data)
			return res.status(404).json({ 
				error: true, 
				data: { mensaje: 'No hay dato registrados' } 
			});

		return res.status(200).json({
			error: false,
			data: data
		});
	})
	.catch(function (err) {
     	return res.status(500).json({
			error: true,
			data: { mensaje: err.message }
		});
    });
}

function saveComentario(req, res, next){
	console.log(JSON.stringify(req.body));

	Comentario.forge({ id_cliente:req.body.id_cliente ,id_respuesta:req.body.id_respuesta ,contenido:req.body.contenido ,respuesta:req.body.respuesta ,id_tipo_comentario:req.body.id_tipo_comentario  })
	.save()
	.then(function(data){
		res.status(200).json({
			error: false,
			data: [{
				msg: "Registro Creado"
			}]
		});
	})
	.catch(function (err) {
		res.status(500)
		.json({
			error: true,
			data: {message: err.message}
		});
	});
}

function getComentarioById(req, res, next) {
	const id = Number.parseInt(req.params.id);
	if (!id || id == 'NaN') 
		return res.status(400).json({ 
			error: true, 
			data: { mensaje: 'Solicitud incorrecta' } 
		});

	Comentario.forge({ id_comentario: id, estatus: 1 })
	.fetch({ withRelated: ['cliente','respuesta','tipo_comentario']})
	.then(function(data) {
		if(!data) 
			return res.status(404).json({ 
				error: true, 
				data: { mensaje: 'dato no encontrado' } 
			});
		return res.status(200).json({ 
			error : false, 
			data : data 
		});
	})
	.catch(function(err){
		return res.status(500).json({ 
			error: false, 
			data: { mensaje: err.message } 
		})
	});
}

function updateComentario(req, res, next) {
	const id = Number.parseInt(req.params.id);
	if (!id || id == 'NaN') {
		return res.status(400).json({ 
			error: true, 
			data: { mensaje: 'Solicitud incorrecta' } 
		});
	}

	Comentario.forge({ id_comentario: id, estatus: 1 })
	.fetch()
	.then(function(data){
		if(!data) 
			return res.status(404).json({ 
				error: true, 
				data: { mensaje: 'Solicitud no encontrada' } 
			});
		data.save({ id_cliente:req.body.id_cliente || data.get('id_cliente'),id_respuesta:req.body.id_respuesta || data.get('id_respuesta'),contenido:req.body.contenido || data.get('contenido'),respuesta:req.body.respuesta || data.get('respuesta'),id_tipo_comentario:req.body.id_tipo_comentario || data.get('id_tipo_comentario') })
		.then(function() {
			return res.status(200).json({ 
				error: false, 
				data: data
			});
		})
		.catch(function(err) {
			return res.status(500).json({ 
				error : true, 
				data : { mensaje : err.message } 
			});
		})
	})
	.catch(function(err) {
		return res.status(500).json({ 
			error : true, 
			data : { mensaje : err.message } 
		});
	})
}

function deleteComentario(req, res, next) {
	const id = Number.parseInt(req.params.id);
	if (!id || id == 'NaN') {
		return res.status(400).json({ 
			error: true, 
			data: { mensaje: 'Solicitud incorrecta' } 
		});
	}
	Comentario.forge({ id_comentario: id, estatus: 1 })
	.fetch()
	.then(function(data){
		if(!data) 
			return res.status(404).json({ 
				error: true, 
				data: { mensaje: 'Solicitud no encontrad0' } 
			});

		data.save({ estatus:  0 })
		.then(function() {
			return res.status(200).json({ 
				error: false,
				data: { mensaje: 'Registro eliminado' } 
			});
		})
		.catch(function(err) {
			return res.status(500).json({ 
				error: true, 
				data: { mensaje: err.message} 
			});
		})
	})
	.catch(function(err){
		return res.status(500).json({ 
			error: true, 
			data: { mensaje: err.message } 
		});
	})
}

