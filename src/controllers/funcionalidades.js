'use strict';

const Funcionalidades 	= require('../collections/funcionalidades');
const Funcionalidad  	= require('../models/funcionalidad');

function getFuncionalidades(req, res, next) {
	Funcionalidades.query(function (qb) {
   		qb.where('funcionalidad.estatus', '=', 1);
	})
	.fetch({ columns: ['id_funcionalidad','id_funcionalidad_padre','nombre','icono','orden','nivel','estatus','url_vista'] })
	.then(function(data) {
		if (!data)
			return res.status(404).json({ 
				error: true, 
				data: { mensaje: 'No hay dato registrados' } 
			});

		return res.status(200).json({
			error: false,
			data: data
		});
	})
	.catch(function (err) {
     	return res.status(500).json({
			error: true,
			data: { mensaje: err.message }
		});
    });
}

function saveFuncionalidad(req, res, next){
	console.log(JSON.stringify(req.body));

	Funcionalidad.forge({ id_funcionalidad_padre:req.body.id_funcionalidad_padre ,nombre:req.body.nombre ,icono:req.body.icono ,orden:req.body.orden ,nivel:req.body.nivel ,estatus:req.body.estatus ,url_vista:req.body.url_vista  })
	.save()
	.then(function(data){
		res.status(200).json({
			error: false,
			data: data
		});
	})
	.catch(function (err) {
		res.status(500)
		.json({
			error: true,
			data: {message: err.message}
		});
	});
}

function getFuncionalidadById(req, res, next) {
	const id = Number.parseInt(req.params.id);
	if (!id || id == 'NaN') 
		return res.status(400).json({ 
			error: true, 
			data: { mensaje: 'Solicitud incorrecta' } 
		});

	Funcionalidad.forge({ id_funcionalidad: id })
	.fetch()
	.then(function(data) {
		if(!data) 
			return res.status(404).json({ 
				error: true, 
				data: { mensaje: 'dato no encontrado' } 
			});
		return res.status(200).json({ 
			error : false, 
			data : data 
		});
	})
	.catch(function(err){
		return res.status(500).json({ 
			error: false, 
			data: { mensaje: err.message } 
		})
	});
}

function updateFuncionalidad(req, res, next) {
	const id = Number.parseInt(req.params.id);
	if (!id || id == 'NaN') {
		return res.status(400).json({ 
			error: true, 
			data: { mensaje: 'Solicitud incorrecta' } 
		});
	}

	Funcionalidad.forge({ id_funcionalidad: id })
	.fetch()
	.then(function(data){
		if(!data) 
			return res.status(404).json({ 
				error: true, 
				data: { mensaje: 'Solicitud no encontrada' } 
			});
		data.save({ id_funcionalidad_padre:req.body.id_funcionalidad_padre || data.get('id_funcionalidad_padre'),nombre:req.body.nombre || data.get('nombre'),icono:req.body.icono || data.get('icono'),orden:req.body.orden || data.get('orden'),nivel:req.body.nivel || data.get('nivel'),estatus:req.body.estatus || data.get('estatus'),url_vista:req.body.url_vista || data.get('url_vista') })
		.then(function() {
			return res.status(200).json({ 
				error: false, 
				data: data
			});
		})
		.catch(function(err) {
			return res.status(500).json({ 
				error : true, 
				data : { mensaje : err.message } 
			});
		})
	})
	.catch(function(err) {
		return res.status(500).json({ 
			error : true, 
			data : { mensaje : err.message } 
		});
	})
}

function deleteFuncionalidad(req, res, next) {
	const id = Number.parseInt(req.params.id);
	if (!id || id == 'NaN') {
		return res.status(400).json({ 
			error: true, 
			data: { mensaje: 'Solicitud incorrecta' } 
		});
	}
	Funcionalidad.forge({ id_funcionalidad: id })
	.fetch()
	.then(function(data){
		if(!data) 
			return res.status(404).json({ 
				error: true, 
				data: { mensaje: 'Solicitud no encontrad0' } 
			});

		data.save({ estatus:  0 })
		.then(function() {
			return res.status(200).json({ 
				error: false,
				data: { mensaje: 'Registro eliminado' } 
			});
		})
		.catch(function(err) {
			return res.status(500).json({ 
				error: true, 
				data: { mensaje: err.message} 
			});
		})
	})
	.catch(function(err){
		return res.status(500).json({ 
			error: true, 
			data: { mensaje: err.message } 
		});
	})
}

