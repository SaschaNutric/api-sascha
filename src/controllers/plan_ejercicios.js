'use strict';

const PlanEjercicios = require('../collections/plan_ejercicios');
const PlanEjercicio  = require('../models/plan_ejercicio');

function getPlanEjercicios(req, res, next) {
	PlanEjercicios.query(function (qb) {
   		qb.where('plan_ejercicio.estatus', '=', 1);
	})
	.fetch({ columns: ['id_plan_ejercicio', 'nombre', 'descripcion', 'fecha_creacion', 'fecha_actualizacion', 'estatus'] })
	.then(function(data) {
		if (!data)
			return res.status(404).json({ 
				error: true, 
				data: { mensaje: 'No hay servicios registrados' } 
			});

		return res.status(200).json({
			error: false,
			data: data
		});
	})
	.catch(function (err) {
     	return res.status(500).json({
			error: true,
			data: { mensaje: err.message }
		});
    });
}

function savePlanEjercicio(req, res, next){
	PlanEjercicio.forge({
        nombre: req.body.nombre,
        descripcion: req.body.descripcion
	})
	.save()
	.then(function(data){
		res.status(200).json({
			error: false,
			data: data
		});
	})
	.catch(function (err) {
		res.status(500)
		.json({
			error: true,
			data: {message: err.message}
		});
	});
}

function getPlanEjercicioById(req, res, next) {
	const id = Number.parseInt(req.params.id);
	if (!id || id == 'NaN') 
		return res.status(400).json({ 
			error: true, 
			data: { mensaje: 'Solicitud incorrecta' } 
		});

	PlanEjercicio.forge({ id_plan_ejercicio: id, estatus: 1 })
	.fetch()
	.then(function(data) {
		if(!data) 
			return res.status(404).json({ 
				error: true, 
				data: { mensaje: 'Servicio no encontrado' } 
			});
		return res.status(200).json({ 
			error : false, 
			data : data 
		});
	})
	.catch(function(err){
		return res.status(500).json({ 
			error: false, 
			data: { mensaje: err.message } 
		})
	});
}

